public class MDiaFeriado {
    private int dia;
    private int mes;
    private int anio;
    private String descripcion;
    private String fecha;
    private char estatus;

    public MDiaFeriado() {
    }

    public MDiaFeriado(int dia, int mes, int anio, String descripcion, String fecha, char estatus) {
        this.dia = dia;
        this.mes = mes;
        this.anio = anio;
        this.descripcion = descripcion;
        this.fecha = fecha;
        this.estatus = estatus;
    }



    public int getDia() {
        return dia;
    }

    public void setDia(int dia) {
        this.dia = dia;
    }

    public int getMes() {
        return mes;
    }

    public void setMes(int mes) {
        this.mes = mes;
    }

    public int getAnio() {
        return anio;
    }

    public void setAnio(int anio) {
        this.anio = anio;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    public String getFecha() {
        return fecha;
    }

    public void setFecha(String fecha) {
        this.fecha = fecha;
    }

    public char getEstatus() {
        return estatus;
    }

    public void setEstatus(char estatus) {
        this.estatus = estatus;
    }
    
    
    
}
